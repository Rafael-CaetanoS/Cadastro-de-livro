	private void preencherTabela () {
	    LivroDAO livro = new LivroDAO();
	    List<Livro> listaLivros = livro.getLivro();

	    DefaultTableModel tabelaLivros = (DefaultTableModel) jTable1.getModel();

	    for(Livro l : listaLivros) {
	        Object[] obj = new Object[] {
	                l.getIsbn(),
	                l.getTitulo(),
	                l.getAutor(),
	                l.getAno(),
	                l.getEditora()
	        };
	        tabelaLivros.addRow(obj);
	    }
	}
	
	
