    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {                                        
    	if(jTable1.getSelectedRow()== -1){
    		JOptionPane.showMessageDialog(this, "Por favor, selecione uma linha para excluir.");
    	}
    	else{
    		Livro l = new Livro();
    		LivroDAO dao = new LivroDAO();
    		l.setIsbn((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
    		dao.excluirLivro(l.getIsbn());
    		int linha = jTable1.getSelectedRow();
    		DefaultTableModel tabela = (DefaultTableModel) jTable1.getModel();
    		tabela.removeRow(linha);
    		JOptionPane.showMessageDialog(this, "Livro excluido");
      }
    }                                       

